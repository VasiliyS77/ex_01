import math


def induced_voltage(I, D, r, l, f):
    """
    Расчёт наведённого напряжения между параллельными линиями.

    Параметры:
        I — ток в рабочей линии, А
        D — расстояние между линиями, м
        r — радиус провода, м
        l — длина участка, м
        f — частота сети, Гц
    Возвращает:
        Наведённое напряжение (действующее значение), В
    """
    mu0 = 4 * math.pi * 1e-7  # магнитная постоянная, Гн/м
    w = 2 * math.pi * f  # круговая частота
    M_per_m = (mu0 / (2 * math.pi)) * math.log(D / r)
    M = M_per_m * l
    E = w * M * I  # действующее значение наведённого напряжения
    return E


# Пример:
I = 4500.0  # ток в рабочей линии, А
D = 15.0  # расстояние между линиями, м
r = 0.20  # радиус провода, м
l = 150.0  # длина линии, м (10 км)
f = 50.0  # частота сети, Гц

E = induced_voltage(I, D, r, l, f)
print(f"Наведённое напряжение: {E:.1f} В")
